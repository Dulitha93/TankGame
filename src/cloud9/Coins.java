
package cloud9;

public class Coins {

    private int xcod;
    private int ycod;
    private int time;
    private int val;

    public void Coins() {
        this.xcod = 0;
        this.ycod = 0;
        this.time = 0;
        this.val = 0;

    }

    public int getXcod() {
        return xcod;
    }

    public void setXcod(int xcod) {
        this.xcod = xcod;
    }

    public int getYcod() {
        return ycod;
    }

    public void setYcod(int ycod) {
        this.ycod = ycod;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

}
